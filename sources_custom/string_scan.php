                if ($is_admin_string) {
                    $lang_strings_admin[] = $str;
                } elseif ($is_non_admin_string) {
                    $lang_strings_non_admin[] = $str;
                } else {
                    $lang_strings_unknown[] = $str;
                }
            }
        }
    }
    closedir($dh);

